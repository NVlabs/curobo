class MCLookupTables:
    """Marching cubes lookup tables as Warp arrays.

    Provides lazy initialization of lookup tables for GPU usage.
    Tables are cached per-device to support multi-GPU setups.
    """

    _instances: Dict[str, "MCLookupTables"] = {}

    def __init__(self, device: torch.device):
        """Initialize lookup tables on the specified device.

        Args:
            device: Torch device for table allocation.
        """
        init_warp()

        self.device = device
        device_str = str(device)

        # Number of triangles per configuration (256 entries)
        self.num_tris_table = wp.array(
            NUM_TRIANGLES_TABLE, dtype=wp.int32, device=device_str
        )

        # Triangle table (256 × 16 = 4096 entries, flattened)
        self.tri_table = wp.array(
            np.array(TRIANGLE_TABLE).flatten(), dtype=wp.int32, device=device_str
        )

        # Edge ownership table (12 edges × 4 values = 48 entries)
        self.edge_owner = wp.array(
            EDGE_OWNER_OFFSETS, dtype=wp.int32, device=device_str
        )

    @classmethod
    def get(cls, device: torch.device) -> "MCLookupTables":
        """Get or create lookup tables for the specified device.

        Args:
            device: Torch device.

        Returns:
            MCLookupTables instance for the specified device.
        """
        key = str(device)
        if key not in cls._instances:
            cls._instances[key] = cls(device)
        return cls._instances[key]


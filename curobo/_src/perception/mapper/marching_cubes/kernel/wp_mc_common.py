@wp.func
def interpolate_edge_vertex(
    p0: wp.vec3,
    p1: wp.vec3,
    s0: wp.float32,
    s1: wp.float32,
) -> wp.vec3:
    """Interpolate vertex position along an edge between two corners.

    Computes the zero-crossing position between p0 (with SDF value s0)
    and p1 (with SDF value s1) using linear interpolation.

    Args:
        p0: First corner position.
        p1: Second corner position.
        s0: SDF value at first corner.
        s1: SDF value at second corner.

    Returns:
        Interpolated position at the zero crossing.
    """
    if wp.abs(s0 - s1) < 1e-10:
        return p0
    t = -s0 / (s1 - s0)
    t = wp.clamp(t, 0.0, 1.0)
    return p0 + t * (p1 - p0)


@wp.func
def get_edge_vertex(
    edge_id: wp.int32,
    p0: wp.vec3,
    p1: wp.vec3,
    p2: wp.vec3,
    p3: wp.vec3,
    p4: wp.vec3,
    p5: wp.vec3,
    p6: wp.vec3,
    p7: wp.vec3,
    s0: wp.float32,
    s1: wp.float32,
    s2: wp.float32,
    s3: wp.float32,
    s4: wp.float32,
    s5: wp.float32,
    s6: wp.float32,
    s7: wp.float32,
) -> wp.vec3:
    """Get interpolated vertex position for a given edge.

    Standard marching cubes edge numbering (12 edges):
        Edge 0:  p0-p1   Edge 1:  p1-p2   Edge 2:  p2-p3   Edge 3:  p3-p0
        Edge 4:  p4-p5   Edge 5:  p5-p6   Edge 6:  p6-p7   Edge 7:  p7-p4
        Edge 8:  p0-p4   Edge 9:  p1-p5   Edge 10: p2-p6   Edge 11: p3-p7

    Args:
        edge_id: Edge index (0-11).
        p0-p7: Corner positions.
        s0-s7: SDF values at corners.

    Returns:
        Interpolated vertex position on the edge.
    """
    v = wp.vec3(0.0, 0.0, 0.0)

    if edge_id == 0:
        v = interpolate_edge_vertex(p0, p1, s0, s1)
    elif edge_id == 1:
        v = interpolate_edge_vertex(p1, p2, s1, s2)
    elif edge_id == 2:
        v = interpolate_edge_vertex(p2, p3, s2, s3)
    elif edge_id == 3:
        v = interpolate_edge_vertex(p3, p0, s3, s0)
    elif edge_id == 4:
        v = interpolate_edge_vertex(p4, p5, s4, s5)
    elif edge_id == 5:
        v = interpolate_edge_vertex(p5, p6, s5, s6)
    elif edge_id == 6:
        v = interpolate_edge_vertex(p6, p7, s6, s7)
    elif edge_id == 7:
        v = interpolate_edge_vertex(p7, p4, s7, s4)
    elif edge_id == 8:
        v = interpolate_edge_vertex(p0, p4, s0, s4)
    elif edge_id == 9:
        v = interpolate_edge_vertex(p1, p5, s1, s5)
    elif edge_id == 10:
        v = interpolate_edge_vertex(p2, p6, s2, s6)
    elif edge_id == 11:
        v = interpolate_edge_vertex(p3, p7, s3, s7)

    return v



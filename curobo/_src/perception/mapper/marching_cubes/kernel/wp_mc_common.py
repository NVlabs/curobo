@wp.func
def local_edge_to_array_idx(local_edge: wp.int32) -> wp.int32:
    """Map local edge (0, 3, 8) to array index (0, 1, 2).

    Each voxel owns 3 edges: 0, 3, and 8.
    This maps them to contiguous array indices for storage.

    Args:
        local_edge: Edge index (0, 3, or 8).

    Returns:
        Array index (0, 1, or 2).
    """
    if local_edge == 0:
        return 0
    elif local_edge == 3:
        return 1
    return 2  # edge 8


@wp.func
def array_idx_to_local_edge(array_idx: wp.int32) -> wp.int32:
    """Map array index (0, 1, 2) back to local edge (0, 3, 8).

    Args:
        array_idx: Array index (0, 1, or 2).

    Returns:
        Local edge index (0, 3, or 8).
    """
    if array_idx == 0:
        return 0
    elif array_idx == 1:
        return 3
    return 8


@wp.func
def binary_search_int32(
    sorted_keys: wp.array(dtype=wp.int32),
    n: wp.int32,
    target: wp.int32,
) -> wp.int32:
    """Binary search for target in sorted array.

    Args:
        sorted_keys: Sorted array of int32 keys.
        n: Number of elements in array.
        target: Value to search for.

    Returns:
        Index of target in array, or -1 if not found.
    """
    left = wp.int32(0)
    right = n - wp.int32(1)
    result = wp.int32(-1)

    # Unrolled binary search for up to 2^24 elements (~16M)
    for _ in range(24):
        if left <= right:
            mid = (left + right) // wp.int32(2)
            val = sorted_keys[mid]
            if val == target:
                result = mid
                left = right + wp.int32(1)  # Exit loop
            elif val < target:
                left = mid + wp.int32(1)
            else:
                right = mid - wp.int32(1)

    return result


